  /**
   * Draft.js用のブロックキーを生成
   */
  function generateBlockKey() {
    const chars = 'abcdefghijklmnopqrstuvwxyz0123456789';
    let key = '';
    for (let i = 0; i < 5; i++) {
      key += chars.charAt(Math.floor(Math.random() * chars.length));
    }
    return key;
  }

  /**
   * 入院時指示の内容をDraft.js形式に変換
   * フォーマット例:
   *   ＜入院時指示＞
   *   【食事】　　　常食（1600kcal）
   *   【安静度】　　フリー
   *   ...
   *   【その他】
   *   点滴ルートキープ
   */
  function instructionToDraftJs(formData) {
    const blocks = [];

    // 各項目（ラベルと値を全角スペースで揃える）
    // 最長ラベル「モニター」「補助具」は4文字、他は2〜3文字
    // 【食事】　　　 → 4文字 + 全角3つ
    // 【安静度】　　 → 5文字 + 全角2つ
    // 【モニター】　 → 6文字 + 全角1つ
    // 【尿測】　　　 → 4文字 + 全角3つ
    // 【保清】　　　 → 4文字 + 全角3つ
    // 【補助具】　　 → 5文字 + 全角2つ
    const items = [
      { label: '【食事】', padding: '　　　', value: formData.meal, detail: formData.mealDetail },
      { label: '【安静度】', padding: '　　', value: formData.activity },
      { label: '【モニター】', padding: '　', value: formData.monitor },
      { label: '【尿測】', padding: '　　　', value: formData.urine },
      { label: '【保清】', padding: '　　　', value: formData.bathing },
      { label: '【補助具】', padding: '　　', value: formData.mobility }
    ];

    for (const item of items) {
      let displayValue = item.value;
      if (item.detail) {
        displayValue = `${item.value}（${item.detail}）`;
      }
      const text = `${item.label}${item.padding}${displayValue}`;

      blocks.push({
        key: generateBlockKey(),
        type: 'unstyled',
        text: text,
        depth: 0,
        inlineStyleRanges: [],
        entityRanges: [],
        data: {}
      });
    }

    // 自由記述（あれば）
    if (formData.freeText) {
      // 空行
      blocks.push({
        key: generateBlockKey(),
        type: 'unstyled',
        text: '',
        depth: 0,
        inlineStyleRanges: [],
        entityRanges: [],
        data: {}
      });

      // 【その他】見出し
      blocks.push({
        key: generateBlockKey(),
        type: 'unstyled',
        text: '【その他】',
        depth: 0,
        inlineStyleRanges: [],
        entityRanges: [],
        data: {}
      });

      // 改行で分割して各行を追加
      const lines = formData.freeText.split('\n');
      for (const line of lines) {
        blocks.push({
          key: generateBlockKey(),
          type: 'unstyled',
          text: line,
          depth: 0,
          inlineStyleRanges: [],
          entityRanges: [],
          data: {}
        });
      }
    }

    return JSON.stringify({ blocks, entityMap: {} });
  }

